				<b>Bilan du bureau 2011 :</b><br>	
				L'année du bureau 2011 a été marquée par plusieurs points. Tout d'abord,  par la création d'un nouveau site basé sur la structure et les bases de données de l'ancien site. Ainsi, un nouveau logo a été réalisé impliquant un changement de charte graphique et une page dédiée aux supports de cours a été mise en place.
				D'autre part,  des contacts ont été pris avec différentes entreprises, notamment celles embauchant ou ayant embauché des membres de l'A2MAIM avec lesquelles le bureau était en contact.
				Enfin, une action d'information auprès des étudiants a été menée &agrave partir de la reprise des enseignements. Cette action visait principalement les étudiants du MPE mais s'adressait également aux étudiants d'IFMA. Il semble que ces actions aient eu un effet bénéfique sur la dynamique de l'association, notamment gr&acirc;ce &agrave l'établissement d'un système de parrainage. Les relations parrain-fillot de ceux qui se sont intéressés &agrave cette action se sont révélées être bonnes. Les parrains ont souvent pu donner des réponses aux questions que se posaient leurs fillots et qu'ils s'étaient eux même posées lors de leur derni ère année de master.
				Lors de l'année 2011-2012, le nombre d'inscrits sur le site n'a eu de cesse d'augmenter. Nous avons pu noter l'arrivée d'étudiants de la promotion MPE12 mais également d'étudiants IFMA12. On peut évoquer quelques chiffres : depuis la rentrée 2012, le site a été visité 1234 fois. Parmi ces visites, 70% concernaient les supports de cours et les annonces de stage.<br><br>

				<b>Actions &agrave poursuivre/créer :</b><br>	
