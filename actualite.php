				<li><h5>Obtention du titre d'ingénieur MACS par VAE</h5></li>
				<p>Les étudiants diplomés du M2 Ingénierie mathématique de l'UPMC ayant 3 ans d'expérience peuvent désormais obtenir le titre d'ingénieur (reconnu par la CTI, Commission des Titres d'Ingénieur) par VAE (Validation des acquis de l'expérience). Vous pouvez, par la VAE, obtenir ce titre d'ingénieur (proposé par le directeur du diplôme d'ingénieur MACS "Mathématiques Appliquées et Calcul Scientifique" à SupGalilée (Paris 13 - Nord) ) en déposant un dossier qui devra mentionner l'expérience professionnelle et attester du niveau B2 au TOEIC. Les caractéristiques du diplôme de M2 ne sont pas nécessaires, son programme étant tout a fait conforme aux attentes. Les modalités figurent ci-dessous.</p>

				<p>Constitution d’un dossier de VAE pour avoir le titre d’ingénieur MACS (reconnu par la CTI) après le dess de mathématiques appliquées : </p>
	 
				<p>Les anciens élèves du master mention mathématiques et applications, spécialité ingéniérie mathématique, parcours MPE et parcours IFMA de l'UPMC Paris 6 rentrent dans le cadre du décret de 2002 (validation sans reprendre des études). Il en est de même pour les titulaires du DESS de mathématiques appliquées. Ils doivent cependant passer un entretien oral devant le jury de VAE, constitué en partie par le responsable du diplôme d’ingénieur MACS, qui recevra les candidats auparavant.
				Les conditions d’éligibilité pour le diplôme ingénieur MACS de l’université Paris 13 : obtention du master précité ou du DESS de mathématiques appliquées de l’université Paris 6 (UPMC), ainsi que 3 années d’expérience en tant que modélisateur, développeur en mathématiques appliquées ou pour les mathématiques financières, ou tout emploi utilisant des compétences avancées de mathématiques appliquées, de calcul scientifique et numérique…
				Il faut se connecter sur www.validexper.fr afin de s‘inscrire à une réunion d’information, et de connaitre les pièces de la procédure. Il y a nécessairement un CV détaillé et un dossier de préorientation à soumettre.
				Des aides financières d’Etat existent pour effectuer cette validation.
