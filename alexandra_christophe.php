<h3>Alexandra Christophe - Thèse </h3>
<p>
Après avoir suivi le parcours Mécanique du master MPE, mon stage s'est déroulé au sein de
l'ONERA Châtillon. Le sujet était orienté analyse numérique et portait notamment sur l'étude de
schémas numériques en temps appliqués à de la modélisation en aéroacoustique (les notions
abordées étaient : méthode des volumes finis, méthode de Galerkin discontinue, schéma de RungeKutta et décomposition de domaine) . Par la suite, j'ai débuté une thèse financée par le CEA traitant
de la mise en œuvre d'une méthode de décomposition de domaine, avec recouvrement, en
électromagnétisme (notions abordées : méthode de décomposition de domaine, équations de
Maxwell et méthode des éléments finis).
La formation proposée par le master MPE me permet d'avoir un profil complet et de bon niveau tant
au niveau numérique qu'informatique. Il ne m'a pas été difficile de décrocher le stage puis la thèse. </p><h3></h3>
