	<h3>Statuts<br><br></h3>
		<h6>Titre I. But et composition<br></h6>
		<h7>Art. 1er. Formation<br></h7>Il est fondé entre les adhérents aux présents statuts une association régie par la loi du 
		1er juillet 1901 et le décret du 16 août 1901, ayant pour titre : "Association des Anciens du Master d'Ingénierie Mathématique
	   de l'université Pierre et Marie Curie'', également appelée A2MAIM.<br><br>
	   
	   	<h7> Art. 2. Objet<br></h7>L'association a pour buts :
		<li>de créer et favoriser entre les anciens étudiants du parcours "Mathématiques pour l'Entreprise" de la spécialité 
		"Ingénierie mathématique" de la mention "Mathématiques et applications" du Master sciences et technologie de 
		l'Université Pierre et Marie Curie (ex DESS de Mathématiques Appliquées de l'Université Pierre et Marie Curie) 
		un lien de solidarité durable ainsi qu'une entraide amicale ;</li>
     	<li>de favoriser les échanges entre les anciens dans le but d'aider à l'insertion professionnelle des jeunes diplômés ;</li>
      	<li>d'être un lien entre les anciens étudiants, la direction de l'université et le monde de l'entreprise.</li><br><br>
      
      	<h7>  Art. 3. Siège social  <br></h7> Le siège social est fixé à 4 place Jussieu 75005 PARIS.<br>
		Il pourra être transféré en tout autre lieu sur simple décision du Conseil d'Administration. <br><br>
		 
		<h7>Art. 4. Durée<br></h7>La durée de l'association est illimitée.  <br><br>
		 
		<h7> Art. 5. Membres  <br></h7> L'association se compose de membres d'honneur, de membres actifs et de membres passifs.
		<li>Sont membres d'honneur les personnes physiques ou morales qui ont rendu des services signalés à l'association. 
		Ils sont désignés par décision du Conseil, devant être validée par l'Assemblée générale suivante. 
		Ils sont dispensés du paiement de la cotisation annuelle.</li>
      	<li>Sont membres actifs les anciens qui adhèrent aux présents statuts et qui s'engagent à verser une cotisation 
      	annuelle fixée chaque année par l'Assemblée Générale.</li>
      	<li>Sont membres passifs les étudiants inscrit de manière régulière au master et qui en font la demande. 
      	Ils n'ont pas le droit de vote aux Assemblées Générales et ne peuvent pas être élus au Conseil d'Administration. 
      	Ils ne bénéficient parmi les services de l'association que de ceux relatifs à la recherche de stage.</li><br><br>
      
      	<h7> Art. 6. Conditions d'adhésion et de radiation<br></h7>Pour faire partie de l'association, il faut être 
      	titulaire du diplôme correspondant au parcours "Mathématiques pour l'Entreprise" de la spécialité "Ingénierie mathématique" 
      	de la mention "Mathématiques et applications" du Master sciences et technologie de l'Université Pierre et Marie Curie 
      	(ou au DESS de Mathématiques Appliquées de l'Université Pierre et Marie Curie) et être agréé par le bureau qui statue 
      	sur les demandes d'admissions présentées. <br>La qualité de membres de l'association se perd par : 
		<li>décès ;</li>
      	<li>démission, par lettre adressée au président ;</li>
      	<li>radiation, prononcée par le conseil d'administration soit par défaut de paiement de cotisation, soit pour motif grave; 
      	avant de prononcer cette radiation, le Conseil doit laisser la possibilité à 
      	l'intéressé(e) de fournir toute justification nécessaire.</li><br><br>
      
      	<h7>  Art. 7. Moyens  <br></h7>Pour arriver à ses buts, l'Association se donne les moyens suivants : 
		<li>création d'une base de données ;</li>
      	<li>création et diffusion d'un annuaire remis à jour annuellement ;</li>
      	<li>mise en place d'un serveur web accessible aux membres et permettant l'accès à des forums de 
      	discussions, des informations et servant de vitrine à l'Association ;</li>
      	<li>tout autre moyen opportun.</li><br><br>
      
      	<h7> Art. 8. Ressources  <br></h7>Les ressources de l'Association comprennent : 
		<li>le montant des cotisations ;</li>
     	<li>les subventions de l'Etat, de l'université, des collectivités territoriales ;</li>
      	<li>toutes autres ressources légales.</li><br><br>
      
      	<h6>Titre II Administration et fonctionnement<br></h6>
      
      	<h7> Art. 9. Conseil d'administration<br></h7>L'Association est administrée par un Conseil d'Administration 
      	composé d'au plus sept membres, élus l'assemblée générale pour une durée de trois ans. Les membres sont limités 
      	à deux mandats consécutifs.<br>Le conseil est renouvelé chaque année par tiers.
		<br>En cas de vacances, le conseil pourvoit provisoirement au remplacement de ses membres. Il est procédé à leur 
	   	remplacement définitif par la plus prochaine assemblée générale. <br> Est éligible au conseil d'administration tout 
	  	membre actif à jour de ses cotisations et ne faisant pas l'objet d'une mesure d'exclusion. Le conseil choisit parmi 
	   	ses membres un Bureau composé de :
		<li>un président et, éventuellement, un vice-président ;</li>
      	<li>un secrétaire ;</li>
      	<li>un trésorier.</li> qui sont chargés de la gestion quotidienne de l'association. Ils ont pour charge de traiter
       	et d'exécuter les décisions approuvées par le conseil d'administration et de lui proposer des actions. <br><br>
      
      	<h7>Art. 10.  Réunion du conseil<br></h7>Le conseil d'administration se réunit au moins une fois tous les six mois,
       	ou à la demande du quart de ses membres sur convocation du président. <br>Il peut utiliser les outils de communications
        adaptés pour tenir des réunions via le réseau Internet.<br> Les décisions sont prises à la majorité des voix des membres
        présents ou connectés; en cas de partage, la voix du président est prépondérante.<br> Tout membre du conseil qui, 
        sans excuse, n'aura pas assisté à trois réunions consécutives pourra être considéré comme démissionnaire.<br><br>
         
      	<h7> Art. 11. Assemblée générale ordinaire<br></h7>L'assemblée générale ordinaire comprend tous les membres de l'association
       	et se réunit chaque année au mois de novembre.<br> Quinze jours au moins avant la date fixée, les membres de l'association
        sont convoqués par les soins du président. L'ordre du jour est indiqué sur les convocations. <br>Le président, assisté des
        membres du conseil d'administration, préside l'assemblée et expose la situation morale de l'association. <br> Le trésorier
        rend compte de sa gestion et soumet le bilan à l'approbation de l'assemblée.
		<br>Il est procédé, après épuisement de l'ordre du jour, au remplacement des membres du conseil sortant. <br>
		Les délibérations de l'assemblée générale ordinaire sont prises avec un quorum de la moitié des membres et à 
		la majorité absolue des membres présents ou représentés.<br><br>
	
		<h7> Art. 12. Assemblée générale extraordinaire<br></h7>L'assemblée générale extraordinaire est compétente pour modifier les 
		statuts et pour décider de la dissolution ou de la fusion de l'association. <br> Elle se réunit à la demande d'au moins la 
		moitié plus un de ses membres, ou sur demande du conseil. <br>Quinze jours au moins avant la date fixée, les membres de 
		l'association sont convoqués par les soins du président. L'ordre du jour est indiqué sur les convocations. <br>Les délibérations 
		de l'assemblée générale extraordinaire sont prises avec un quorum de la moitié des membres. Si ce quorum n'est pas atteint, 
		l'assemblée est convoquée de nouveau, à quinze jours d'intervalle. Elle peut alors délibérer quel que soit le nombre de membres 
		présents. La majorité requise est des deux tiers des membres présents ou représentés.<br><br>
		
		<h6>Titre III Dispositions diverses<br></h6>	
		
		<h7> Art. 13. Votes<br></h7>Tous les votes peuvent avoir lieu par correspondance 
		(papier ou électronique) ou par procuration auprès d'un autre membre 
		(mais pas plus d'une procuration par membre présent).<br><br>
	
		<h7>  Art. 14. Rétributions<br></h7>Les membres de l'association ne peuvent recevoir aucune rétribution 
		en raison des fonctions qui leur sont confiées, mais peuvent être remboursés de leur frais engagés pour 
		l'Association après acceptation par le Bureau.<br><br>
	
		<h7> Art. 15. Règlement intérieur<br></h7>Un règlement intérieur peut être établi par le conseil d'administration 
		qui le fait alors approuvé par l'assemblée générale.<br>Ce règlement éventuel est destiné à fixer les divers 
		points non prévus par les statuts, notamment ceux qui ont trait à l'administration interne de l'association.<br><br>
	
		<h7> Art. 16. Dissolution<br></h7>En cas de dissolution prononcée par les deux tiers au moins des membres présents 
		à l'assemblée générale, un ou plusieurs liquidateurs sont nommés par celle-ci et l'actif, s'il y a lieu, est dévolu 
		conformément à l'article 9 de la loi du 1er juillet 1901 et au décret du 16 août 1901.<br><br>
