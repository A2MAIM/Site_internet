<h3>Alban Carraz - Ingénieur d'essai chez Safran - Promotion 2010 </h3>
<p></p>Ayant mené mon stage de fin d'étude au sein de PSA en modélisation aéroacoustique,
j'ai continué dans la voie de la modélisation au sein de l'ONERA en tant qu'ingénieur de recherche 
pour développer des modèles de trajectographie. Afin d'approfondir dans le domaine applicatif de 
la modélisation, je suis, en 2012, au sein du groupe Safran, ingénieur d'essai sur les ordinateurs 
de gestion de vol (Flight Management System, FMS) des avions Airbus utilisant les moyens d'essais mis à 
notre disposition par l'avionneur et ses clients mais également des outils développés pour nous 
ou par nous afin de mener à bien nos essais et validations.<br>
J'ai eu la chance de présider A2MAIM à la sortie du Master, avant de passer la main à Alexandra Christophe. 
Ce fut une expérience enrichissante et qui m'a permis de voir d'un autre point de vue le travail qui devait
 être apporté pour faire de cette association un soutien pour les étudiants du Master. J'espère que ce
 travail perdurera et vous sera utile autant qu'il nous l'a été voire plus. Bon courage, ne baissez pas les bras
 vous tenez le bon bout.<h3></h3>